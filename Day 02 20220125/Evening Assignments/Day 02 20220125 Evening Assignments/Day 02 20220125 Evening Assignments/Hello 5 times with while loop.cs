using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Day_02_20220125_Evening_Assignments
{
    class Program
    {
        static void Main(string[] args)
        {
            // print hello 5times using while loop
            int i;
            i = 1;
            while (i <= 5)
            {
                Console.WriteLine("Hello");
                i++;
            }
            Console.ReadLine();
        }
    }
